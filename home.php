      <h1>What is PCOS?</h1>
      <h5 class="w3-padding-32">Polycystic ovary syndrome (PCOS) is a condition that affects a woman’s hormone levels.

Women with PCOS produce higher-than-normal amounts of male hormones. This hormone imbalance causes them to skip menstrual periods and makes it harder for them to get pregnant.

PCOS also causes hair growth on the face and body, and baldness. And it can contribute to long-term health problems like diabetes and heart disease.

Birth control pills and diabetes drugs can help fix the hormone imbalance and improve symptoms.</h5>

      <p class="w3-text-grey">PCOS is a problem with hormones that affects women during their childbearing years (ages 15 to 44). Between 2.2 and 26.7 percent of women in this age group have PCOS.

Many women have PCOS but don’t know it. In one study, up to 70 percent of women with PCOS hadn’t been diagnosed.

PCOS affects a woman’s ovaries, the reproductive organs that produce estrogen and progesterone — hormones that regulate the menstrual cycle. The ovaries also produce a small amount of male hormones called androgens.</p>
