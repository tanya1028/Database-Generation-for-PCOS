    <div class="w3-panel w3-leftbar w3-light-grey">
      <p><b>Throughout these research papers, the one phrase that sticks out most is insulin resistance. It seems that insulin and insulin resistance play the most prominent role in causing PCOS. This means that putting someone with PCOS on a diet that can lower insulin levels and reduce insulin resistance— like the ketogenic diet — is best, right?</b></p>

<p><b>Before we jump to any conclusions, we must also consider the studies on the inheritability of PCOS. The studies found that this disorder has a genetic influence that is independent of lifestyle factor, which raises the question — are women with PCOS stuck with it?</b></p>

    <img src="https://cdn.ruled.me/wp-content/uploads/2017/09/graphic_pcos-inheritable-yet-mutable.png" style="width:100%;max-width:1000px" class="w3-margin-top">
  <p>Many researchers suggest that there are specific genetic variants that contribute to PCOS. For example, a recent study by Miller and colleagues concluded that we might be able to explain the association of PCOS and insulin resistance by a single genetic defect.</p>

<p>Another group of researchers proposed the theory that fetal adaptations cause the genetic defect(s) that lead(s) to PCOS. In other words, if the fetus is developing inside of its mother while she is in a stressful environment (i.e., lacking adequate food), then the fetus will undergo genetic changes to help it adapt to that stressful environment.</p>

<p>One of these genetic adaptations is insulin resistance. Although insulin resistance is associated with PCOS, heart disease, obesity, and type 2 diabetes, it is useful when food is scarce because it keeps energy in the blood for a longer duration, so it can reach the cells that need it most.</p>
<p>Scientists theorize that some women develop a particular type of insulin resistance that turns off their fertility cycle and increases their testosterone levels to improve their chances of survival. However, this may only cause PCOS symptoms when the woman is chronically stressed, overeats, or is sedentary.</p>

<p>In other words, PCOS genes are inheritable, but having them doesn’t mean that you will be infertile. Later, we will find out how to turn fertility on (and PCOS off), but first, let’s figure out how PCOS happens.</p>
