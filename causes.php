    <h3 class="w3-center w3-padding-48"><span class="w3-tag w3-wide">How Insulin Resistance Causes PCOS</span></h3>

    <div class="w3-panel w3-leftbar w3-light-grey">
      <p>Earlier we found out that women with PCOS tend to have higher levels of insulin and insulin resistance. Elevated levels of insulin cause the ovaries to produce more androgens (like testosterone). Insulin also decreases the production of sex-hormone binding globulin — a glycoprotein that prevents testosterone from freely entering cells.</p>

<p>With more androgen production and less sex-hormone binding globulin, free testosterone can freely float through the blood and interact with cells. This can cause hair growth on the chest and face, mood swings, fatigue, low sex drive, acne, and other PCOS symptoms.</p>

<p>As androgen levels continue to increase, they stimulate 5-alpha reductase activity — an enzyme that converts testosterone to a more potent metabolite called dihydrotestosterone. This makes PCOS symptoms even worse.</p>

<p>Genetics can set the stage for this to happen, but PCOS will not progress without the presence of high insulin levels and insulin resistance. To create a chronic state of insulin resistance and hyperinsulinemia, it takes a combination of poor lifestyle habits.</p>
